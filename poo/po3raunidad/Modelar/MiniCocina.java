
public class MiniCocina
{
   
   private String utencilios;
    public MiniCocina(String utencilios)
    {
        this.utencilios=utencilios;
    }
    public String getMiniCocina()
    {
        return utencilios;
    }
    public void setMiniCocina(String nuevaMiniCocina)
    {
      utencilios = nuevaMiniCocina;
    }
}